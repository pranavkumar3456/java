import java.lang.*;
import java.util.*;

public class array2dbasic {
    public static void main(String[] args)
    {
        int mat[][] = new int[4][3];
        //4 --> no of rows
        //3 --> no of columns.
        //total number of rows in mtrix
        // number of rows --> mat.length
        // number of column --> mat[0].length
        System.out.println(mat.length);
        System.out.println(mat[0].length);
    }

}
